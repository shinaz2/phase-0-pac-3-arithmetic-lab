// Basic Math Functions in JavaScript

// Adds two numbers
function add(a, b) {
    return a + b;
}

// Subtracts b from a
function subtract(a, b) {
    return a - b;
}

// Multiplies two numbers
function multiply(a, b) {
    return a * b;
}

// Divides a by b
function divide(a, b) {
    return a / b;
}

// Increments a number by 1
function increment(n) {
    return n + 1;
}

// Decrements a number by 1
function decrement(n) {
    return n - 1;
}

// Parses a value as an integer (base 10)
function makeInt(n) {
    return parseInt(n, 10);
}

// Parses a value as a floating-point number (keeps decimals)
function preserveDecimal(n) {
    return parseFloat(n);
}
